const resultado = parseFloat(prompt("Escolha uma alternativa: \n1)\n2)\n3)"))

switch (resultado) {
  case 1:
    alert("Você escolheu a alternativa '1'");
    break;
  case 2:
    alert("Você escolheu a alternativa '2'");
    break;
  case 3:
    alert("Você escolheu a alternativa '3'");
    break;
  default:
    alert("Finalizando...")
}

